def normalize(x: str) -> str:
    """Привести ответ модели к slug из закрытого набора.

    Принимает точный slug, slug в нижнем регистре или русский label.
    Всё неизвестное/пустое → other.
    """
    if not x:
        return OTHER
    key = x.strip().lower()
    if key in ALLOWED_SLUGS:
        return key
    if key in _SLUG_BY_LABEL:
        return _SLUG_BY_LABEL[key]
    return OTHER


def label_for(slug: str) -> str:
    """Человекочитаемое название темы по slug."""
    return _LABEL_BY_SLUG.get(slug, _LABEL_BY_SLUG[OTHER])


def emoji_for(slug: str) -> str:
    """Emoji темы по slug."""
    return _EMOJI_BY_SLUG.get(slug, _EMOJI_BY_SLUG[OTHER])


def allowed_block() -> str:
    """Список разрешённых тем для промпта: 'slug — label: описание' построчно."""
    return "\n".join(
        f"{slug} — {label}: {DESCRIPTIONS[slug]}" for label, slug, _ in TOPICS
    )
